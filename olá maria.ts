// Função de multiplicação
function multiplicar(a: number, b: number): number {
    return a * b;
}

// Função de saudação
function saudacao(nome: string): string {
    return "Olá " + nome;
}

// Exemplos de uso
const resultadoMultiplicacao = multiplicar(5, 3); // Retorna 15
const saudacaoNome = saudacao("Maria"); // Retorna "Olá Maria"

console.log(resultadoMultiplicacao); // Saída: 15
console.log(saudacaoNome); // Saída: Olá Maria
